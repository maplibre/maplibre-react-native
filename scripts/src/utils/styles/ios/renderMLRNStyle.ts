Object.keys(iosSpecOverrides).forEach((propName) => {
  const camelCasePropName = camelCase(propName);

  iosPropNameOverrides[camelCasePropName] = camelCase(
    iosSpecOverrides[propName]!,
  );
});

function iosPropName(name: string) {
  if (name.indexOf("visibility") !== -1) {
    return "visible";
  }
  if (name === "fillExtrusionVerticalGradient") {
    return "fillExtrusionHasVerticalGradient";
  }
  if (iosPropNameOverrides[name]) {
    return iosPropNameOverrides[name];
  }
  return name;
}

function iosPropMethodName(layer: any, name: string) {
  if (name.indexOf("Visibility") !== -1) {
    return pascalCase(layer.name) + "StyleLayer" + name;
  }

  return name;
}

function setLayerMethodName(layer: any) {
  return `${camelCase(layer.name)}Layer`;
}

export function renderMLRNStyleH(layers: any[], filePath: string): string {
  const layerMethodDecls = layers
    .map(
      (layer: any) =>
        `- (void)${setLayerMethodName(layer)}:(${getLayerType(layer, "ios")} *)layer withReactStyle:(NSDictionary *)reactStyle isValid:(BOOL (^)(void)) isValid;`,
    )
    .join("\n");

  const propMethodDecls = layers
    .flatMap((layer: any) =>
      layer.properties.flatMap((prop: any) => {
        const layerType = getLayerType(layer, "ios");
        const methodName = iosPropMethodName(layer, pascalCase(prop.name));
        const decls = [
          `- (void)set${methodName}:(${layerType} *)layer withReactStyleValue:(MLRNStyleValue *)styleValue;`,
        ];
        if (prop.transition) {
          decls.push(
            `- (void)set${methodName}Transition:(${layerType} *)layer withReactStyleValue:(MLRNStyleValue *)styleValue;`,
          );
        }
        return decls;
      }),
    )
    .join("\n");

  return `// ${autoGeneratedHeader.doNotModify}
// ${autoGeneratedHeader.generatedFrom(filePath)}

#import "MLRNStyle.h"
#import "MLRNStyleValue.h"

@import MapLibre;

@interface MLRNStyle : NSObject

@property (nonatomic, strong) MLNStyle *style;

- (id)initWithMLNStyle:(MLNStyle*)mlnStyle;

${layerMethodDecls}

${propMethodDecls}

@end
`;
}

export function renderMLRNStyle(layers: any[], filePath: string): string {
  const dispatchMethods = layers
    .map((layer: any) => {
      const propCases: string[] = [];

      layer.properties.forEach((prop: any, i: number) => {
        const methodName = iosPropMethodName(layer, pascalCase(prop.name));
        const prefix = i === 0 ? "  if" : "  } else if";

        let body: string;
        if (prop.image) {
          body = `
    if (![styleValue shouldAddImage]) {
      [self set${methodName}:layer withReactStyleValue:styleValue];
    } else {
      NSString *imageURI = [styleValue getImageURI];

      [MLRNImageLoader fetchImage:imageURI scale:[styleValue getImageScale] sdf:[styleValue getImageSdf] callback:^(NSError *error, UIImage *image) {
        if (image != nil) {
          dispatch_async(dispatch_get_main_queue(), ^{
            if (isValid()) {
              [self->_style setImage:image forName:imageURI];
              [self set${methodName}:layer withReactStyleValue:styleValue];
            }
          });
        }
      }];
    }`;
        } else {
          body = `
    [self set${methodName}:layer withReactStyleValue:styleValue];`;
        }

        propCases.push(
          `${prefix} ([prop isEqualToString:@"${prop.name}"]) {${body}`,
        );

        if (prop.transition) {
          propCases.push(
            `  } else if ([prop isEqualToString:@"${prop.name}Transition"]) {\n    [self set${methodName}Transition:layer withReactStyleValue:styleValue];`,
          );
        }
      });

      propCases.push("  } else {\n  }");

      return `- (void)${setLayerMethodName(layer)}:(${getLayerType(layer, "ios")} *)layer withReactStyle:(NSDictionary *)reactStyle isValid:(BOOL (^)(void)) isValid
{
  if (![self _hasReactStyle:reactStyle]) {
    return;
  }

  NSArray<NSString*> *styleProps = [reactStyle allKeys];
  for (NSString *prop in styleProps) {
    MLRNStyleValue *styleValue = [MLRNStyleValue make:reactStyle[prop]];

${propCases.join("\n")}
  }
}`;
    })
    .join("\n\n");

  const setterMethods = layers
    .flatMap((layer: any) =>
      layer.properties.flatMap((prop: any) => {
        const methodName = iosPropMethodName(layer, pascalCase(prop.name));
        const layerType = getLayerType(layer, "ios");

        let body: string;
        if (layer.name === "light" && prop.name === "position") {
          body = "    layer.position = [styleValue getSphericalPosition];";
        } else if (prop.name === "visibility") {
          body = "    layer.visible = [styleValue isVisible];";
        } else {
          body = `    layer.${iosPropName(prop.name)} = styleValue.mlnStyleValue;`;
        }

        const methods = [
          `- (void)set${methodName}:(${layerType} *)layer withReactStyleValue:(MLRNStyleValue *)styleValue
{
${body}
}`,
        ];

        if (prop.transition) {
          methods.push(
            `- (void)set${methodName}Transition:(${layerType} *)layer withReactStyleValue:(MLRNStyleValue *)styleValue
{
    layer.${iosPropName(prop.name)}Transition = [styleValue getTransition];
}`,
          );
        }

        return methods;
      }),
    )
    .join("\n\n");

  return `// ${autoGeneratedHeader.doNotModify}
// ${autoGeneratedHeader.generatedFrom(filePath)}

#import "MLRNStyle.h"
#import "MLRNUtils.h"
#import "MLRNImageLoader.h"

@implementation MLRNStyle

- (id)initWithMLNStyle:(MLNStyle*)mlnStyle
{
    if (self = [super init]) {
        _style = mlnStyle;
    }
    return self;
}

${dispatchMethods}

${setterMethods}

- (BOOL)_hasReactStyle:(NSDictionary *)reactStyle
{
  return reactStyle != nil && reactStyle.allKeys.count > 0;
}

@end
`;
}
